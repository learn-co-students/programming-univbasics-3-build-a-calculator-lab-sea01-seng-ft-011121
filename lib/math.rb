def addition(num1, num2)
    ans = num1 + num2
    return ans
end

def subtraction(num1, num2)
    ans = num1 - num2
    return ans
end

def division(num1, num2)
    ans = num1 / num2
    return ans
end

def multiplication(num1, num2)
    ans = num1 * num2
    return ans
end

def modulo(num1, num2)
    ans = num1 % num2
    return ans
end

def square_root(num)
    ans = Math.sqrt(num)
    return ans
end